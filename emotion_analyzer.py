import re
import pymorphy3

morph = pymorphy3.MorphAnalyzer()

def lemmatize_text(text: str) -> set:
    if not text:
        return set()
    words = re.findall(r'[а-яё]+', text.lower())
    lemmas = set()
    for word in words:
        try:
            parsed = morph.parse(word)[0]
            lemma = parsed.normal_form
            lemmas.add(lemma)
        except Exception:
            lemmas.add(word)
    return lemmas

