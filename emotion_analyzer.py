def analyze_emotion(text: str):
    if not text or not isinstance(text, str):
        return None
    user_lemmas = lemmatize_text(text)
    if not user_lemmas:
        return None
    best_match = None
    max_matches = 0
    for emotion_key, emotion_data in EMOTION_GENRES.items():
        keywords = emotion_data.get("keywords", [])
        matches = 0
        for keyword in keywords:
            keyword_lemmas = lemmatize_text(keyword)
            if user_lemmas & keyword_lemmas:
                matches += 1
        if matches > max_matches:
            max_matches = matches
            best_match = emotion_key
    if best_match and max_matches > 0:
        emotion_data = EMOTION_GENRES[best_match]
        return (best_match, emotion_data["genres"], emotion_data["label"])
    return None

def get_emotion_by_key(key: str):
    emotion = EMOTION_GENRES.get(key)
    if emotion:
        return (key, emotion["genres"], emotion["label"])
    return None

